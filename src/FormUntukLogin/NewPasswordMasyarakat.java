    private void ButtonGantiAkunActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonGantiAkunActionPerformed
        // TODO add your handling code here:
        komponen();
     
        if(inputUsername.equals(inputConfirmPassword)){
            try {
                String sql = "update tb_petugas set username=?, password=? where id_petugas=?";
                this.pst = connect.connectDB().prepareStatement(sql);
                this.pst.setString(1, inputUsername);
                this.pst.setString(2, inputPassword);
                this.pst.setString(3, inputID);
                this.pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data berhasil Diubah", "Pesan Dari Admin", JOptionPane.INFORMATION_MESSAGE);
                new FormUser().setVisible(true);
            } catch (Exception e) {
            }
        }else if(!inputPassword.equals(inputConfirmPassword)){
            JOptionPane.showMessageDialog(null, "Password Tidak sama");
        }

    }//GEN-LAST:event_ButtonGantiAkunActionPerformed


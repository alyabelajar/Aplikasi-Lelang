    private void FieldGantiUsernameKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_FieldGantiUsernameKeyReleased
        komponen();
        try {
            String sql = "select username from tb_masyarakat where username=?";
            this.pst = connect.connectDB().prepareStatement(sql); 
            this.pst.setString(1, inputUsername);
            this.rs = pst.executeQuery(); 
            if(rs.next()){
                labelError.setVisible(true);
                labelError.setForeground(Color.RED);
                labelError.setText("Username " + inputUsername + " telah digunakan");
             
                
            }else if(inputUsername.equals("")){
                labelError.setVisible(false);
               
               
            }else  if(rs.next() == false){
              labelError.setVisible(true);
              labelError.setText("Username " + inputUsername + " dapat digunakan!");
              labelError.setForeground(Color.GREEN);
              
              FieldGantiPassword.setEditable(true); 
              FieldConfirmPassword.setEditable(true);
              
              
            }
            
            
            
        } catch (Exception e) {
        }
    }//GEN-LAST:event_FieldGantiUsernameKeyReleased

